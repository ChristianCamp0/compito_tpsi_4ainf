#include <stdio.h>
int somma(int n1, int n2){
    int s;
    s=n1+n2;
    return s;
}
int differenza(int n1, int n2){
    int d;
    d=n1-n2;
    return d;
}
int moltiplicazione(int n1, int n2){
    int m;
    m=n1*n2;
    return m;
}
float divisione(int n1, int n2){
    int divi;
    divi=n1/n2;
    return divi;
}